//Comparison Operators

//Equal to
'3' == 3;
console.log('3' == 3);

//Strict Equal to
//Compares the datatype and value of the two values to check if they are equal

console.log('3' === 3); //false

//Not equaal to
console.log('3' != 3); //false

//Strict not equal to
console.log('3' !== 3); //true


//Greater than
console.log(3 > 2); //true


//Less than
console.log(2 < 3); //true

//Greater than or equal to
console.log(3 >= 2); //true

//Less than or equal to
console.log(2 <= 3); //true

//'And' operator
2 && 3;

//'Or' operator
2 || 3;

